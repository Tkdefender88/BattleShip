<template>
  <label class="switch">
    <input type="checkbox" @click="emitToggle" :checked="toggle" />
    <span class="slider"></span>
    <slot></slot>
  </label>
</template>

<script>
export default {
  props: ["toggle"],
  methods: {
    emitToggle(e) {
      this.$emit("onToggle", e.target.checked);
    }
  }
};
</script>

